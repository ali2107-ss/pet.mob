out_dart = """import 'package:flutter/foundation.dart';
import '../models/product.dart';

class ProductProvider with ChangeNotifier {
  final List<Product> _items = [
"""

product_id = 1
for cat_name, items in categories.items():
    for name, desc, price, keywords in items:
        # url
        image_url = f"https://loremflickr.com/500/500/{keywords}?lock={product_id}"
        
        rating = round(random.uniform(4.0, 5.0), 1)
        
        out_dart += f"""    Product(
      id: 'p{product_id}',
      name: '{name}',
      description: '{desc}',
      price: {price},
      category: '{cat_name}',
      imageUrl: '{image_url}',
      rating: {rating},
    ),
"""
        product_id += 1

out_dart += """  ];

  List<Product> get items => [..._items];

  List<Product> getProductsByCategory(String category) {
    if (category == 'Барлығы') return items;
    return _items.where((prod) => prod.category == category).toList();
  }

  Product findById(String id) {
    return _items.firstWhere((prod) => prod.id == id);
  }

  List<Product> search(String query) {
    if (query.isEmpty) return items;
    return _items.where((prod) => prod.name.toLowerCase().contains(query.toLowerCase())).toList();
  }
}
"""

